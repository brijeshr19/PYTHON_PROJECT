#Logical Operators and or not

#Assigment
x=2
x=x+2
print(x)

x+=2
print(x)

x*=2
print(x)

#Multiple assignment
a,b=5,6
print(a)
print(b)

#Unary Operators
n=7
print(-n)

n=-n
print(n)

#Relational Operators
print(a<b)
print(a>b)
print(a==b)
print(a<=b)
print(a!=b)

#Logical Operators
print(a<8 and b<5)
print(a<8 or b<5)

#Reverse the condition with not operator
x=True
x=not x
print(x)

#Decimal to Binary
print(bin(25))#0b11001. 0b represents binary format

#Decimal to Octal
print(oct(25))# 0o31

#Decimal to hex
print(hex(25))#0x19

#hex to decimal
print(0x19)#25

#Bitwise Operator
print(~12)#-13

#Bitwise &
print(12 & 13)

#Bitwise |
print(12 | 13)






